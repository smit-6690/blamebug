"""Incident report output.

Gradio main page uses an iframe (data: URL) so the colorful/dark HTML keeps its colors.
"""

from __future__ import annotations

import base64
import html
import json
import uuid
from datetime import datetime, timezone
from typing import Optional

from blamebug.models import IncidentAnalysis, Severity


def _esc(s: str) -> str:
    return html.escape(s, quote=True)


def json_dumps_safe(obj: dict) -> str:
    try:
        return json.dumps(obj, default=str)[:2000]
    except Exception:
        return "{}"


def build_report_text(
    analysis: IncidentAnalysis,
    raw_logs: str,
    *,
    report_id: Optional[str] = None,
    source: Optional[str] = None,
    metadata: Optional[dict] = None,
) -> str:
    """
    Plain-text report for Gradio (gr.Textbox). No HTML — avoids Gradio's .prose wrapper
    and theme/CSS issues entirely.
    """
    rid = report_id or str(uuid.uuid4())
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    comps = ", ".join(analysis.affected_components) if analysis.affected_components else "—"
    log_preview = raw_logs.strip()
    if len(log_preview) > 8000:
        log_preview = log_preview[:8000] + "\n… [truncated]"

    lines = [
        "══════════════════════════════════════════════════════════════",
        "BLAMEBUG INCIDENT REPORT",
        "══════════════════════════════════════════════════════════════",
        f"Severity : {analysis.severity.value.upper()}",
        f"Title    : {analysis.title}",
        f"Report ID: {rid}",
        f"Time     : {ts}",
        "",
        "──────────────────────────────────────────────────────────────",
        "EXECUTIVE SUMMARY",
        "──────────────────────────────────────────────────────────────",
        analysis.summary,
        "",
        "──────────────────────────────────────────────────────────────",
        "IMPACT",
        "──────────────────────────────────────────────────────────────",
        analysis.impact_assessment,
        f"Affected components: {comps}",
        "",
        "──────────────────────────────────────────────────────────────",
        "ROOT CAUSE (HYPOTHESIS)",
        "──────────────────────────────────────────────────────────────",
        analysis.root_cause_hypothesis,
        f"Confidence: {analysis.confidence:.0%} — verify before acting.",
        "",
        "──────────────────────────────────────────────────────────────",
        "IMMEDIATE ACTIONS",
        "──────────────────────────────────────────────────────────────",
    ]
    for i, step in enumerate(analysis.immediate_actions, 1):
        lines.append(f"{i}. {step}")
    lines.extend(
        [
            "",
            "──────────────────────────────────────────────────────────────",
            "LONG-TERM REMEDIATION",
            "──────────────────────────────────────────────────────────────",
        ]
    )
    for i, step in enumerate(analysis.long_term_remediation, 1):
        lines.append(f"{i}. {step}")
    lines.extend(
        [
            "",
            "──────────────────────────────────────────────────────────────",
            "RAW LOGS",
            "──────────────────────────────────────────────────────────────",
        ]
    )
    if source:
        lines.append(f"Source: {source}")
    if metadata:
        lines.append(f"Metadata: {json_dumps_safe(metadata)}")
    lines.append(log_preview)
    lines.extend(
        [
            "",
            "──────────────────────────────────────────────────────────────",
            "Generated by BlameBug — automated incident analysis.",
        ]
    )
    return "\n".join(lines)


