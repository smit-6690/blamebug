def gradio_html_iframe(full_document: str) -> str:
    """Wrap full HTML in an iframe so Gradio theme CSS can't recolor it."""
    b64 = base64.b64encode(full_document.encode("utf-8")).decode("ascii")
    return (
        '<iframe title="BlameBug report" '
        f'src="data:text/html;charset=utf-8;base64,{b64}" '
        'style="width:100%;min-height:620px;border:1px solid #e5e7eb;'
        'border-radius:10px;overflow:hidden;background:#0b1220;'
        'box-sizing:border-box;"></iframe>'
    )
